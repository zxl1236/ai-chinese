// 工具函数
const QuestionUtils = {
  /**
   * 根据ID获取题目
   */
  getQuestionById(category, articleId, questionId) {
    const articles = QuestionsData[category];
    if (!articles) return null;
    
    const article = articles.find(a => a.id === articleId);
    if (!article) return null;
    
    return article.questions.find(q => q.id === questionId);
  },

  /**
   * 获取分类下的所有文章
   */
  getArticlesByCategory(category) {
    return QuestionsData[category] || [];
  },

  /**
   * 根据标签筛选题目
   */
  getQuestionsByTags(category, tags) {
    const articles = QuestionsData[category] || [];
    const questions = [];
    
    articles.forEach(article => {
      article.questions.forEach(question => {
        if (tags.some(tag => question.tags.includes(tag))) {
          questions.push({
            ...question,
            articleId: article.id,
            articleTitle: article.title
          });
        }
      });
    });
    
    return questions;
  },

  /**
   * 获取难度统计
   */
  getDifficultyStats(category) {
    const articles = QuestionsData[category] || [];
    const stats = { easy: 0, medium: 0, hard: 0 };
    
    articles.forEach(article => {
      if (article.difficulty) {
        stats[article.difficulty]++;
      }
    });
    
    return stats;
  }
};

